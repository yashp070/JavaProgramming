package javaprogramming2;


public class CheckVowelOrConsonant
{
    public static void main(String[] args){
         char c='v';
        if (c=='a'||c=='e'||c=='i'||c=='o'||c=='u')
        {
            System.out.println(c+" is vowel");
        }
        else
        {
            System.out.println(c+" is consonant");
        }

    }
}
