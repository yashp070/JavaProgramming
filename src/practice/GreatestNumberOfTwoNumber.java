package practice;

public class GreatestNumberOfTwoNumber {
	public static void main(String[] args) {
		int n=10;
		int m=0;
		if (n>m) {
			System.out.println(n+" is greatest");
		}else if (n<m) {
			System.out.println(m+" is greatest");
		}else {
			System.out.println(n+" is equals to "+m);
		}
	}
}
